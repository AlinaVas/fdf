#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*arr;
	size_t	i;

	if (n == 0)
		return ;
	arr = (char*)s;
	i = 0;
	while (i < n)
	{
		arr[i] = '\0';
		i++;
	}
}
