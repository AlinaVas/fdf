int		word_cnt(char *str)
{
	char	*s;
	int		in_w;
	int		i;

	s = str;
	in_w = 0;
	i = 0;
	while (*s != '\0')
	{
		if (*s == ' ')
		{
			in_w = 0;
			while (*s == ' ')
				s++;
		}
		else
		{
			if (in_w == 0)
				i++;
			if (in_w == 0)
				in_w = 1;
			s++;
		}
	}
	return (i);
}
